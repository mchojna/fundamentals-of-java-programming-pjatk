public class zadanie_6_3 {

    public static void main(String[] args){

        for(int i = 1; i < 11; i++){
            System.out.println(i);
        }

        int i = 1;

        while(i<11){
            System.out.println(i);
            i++;
        }

        i = 1;

        do{
            System.out.println(i);
            i++;
        }while(i < 11);

    }
    
}
